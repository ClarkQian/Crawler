content = response.text
pagePattern = re.compile('<li.*?class="page-item last.*?<button.*?>(.*?)</button></li>',re.S)
pageRes = pagePattern.findall(content)
page = int(pageRes[0])

findRes = re.findall('<li.*?video-item.*?href="(.*?)".*?title="(.*?)".*?</li>',content, re.S)
res.update(findRes)
print('Hi, I\'m downloading first page belonging to %s: '%(response.url))

#get all pages about this topic
for i in range(2,page+1):

    data['page'] += 1
    response = requests.get(mainUrl,params = data)
    print('Hi, I\'m downloading page %s belonging to %s:'%(i, response.url))
    content = response.text
    findRes = re.findall('<li.*?video-item.*?href="(.*?)".*?title="(.*?)".*?</li>',content, re.S)
    res.update(findRes)

print('Congratualtion! We have just finished task!')
