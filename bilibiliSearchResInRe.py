import requests,re
'''search content'''

#initialization
keyword = input('Please tell us what would you like to search:')
mainUrl = 'https://search.bilibili.com/all?'

page = 1

data = {
    'keyword': keyword,
    'page': page
}

res = dict()

'''process'''

